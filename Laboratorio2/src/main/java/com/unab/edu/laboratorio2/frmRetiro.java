    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double Retiro = Integer.parseInt(txtRetiro.getText());
        if (SaldoTotal >= Retiro) {
            if ((Retiro % 5 == 0)) {
                CLsCuentaUsuario clsCuentas = new CLsCuentaUsuario();
                Cuentausuario cuenta = new Cuentausuario();
                java.util.Date date = new Date();
                double Saldo = Double.parseDouble(txtRetiro.getText());
                cuenta.setIdUsuario(IdUsuario);
                cuenta.setTransaccion(2);
                cuenta.setSaldo(Retiro);
                cuenta.setFecha(date);
                clsCuentas.RealizarTransaccion(cuenta);
                MostrarCuentas();
            } else {
                JOptionPane.showMessageDialog(null, "Solo cantidades divisibles entre 5");
            }

        } else {
            JOptionPane.showMessageDialog(null, "No cuenta con el suficiente Saldo para hacer el Retiro");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

