    void MostrarCuentas() {
        String Titulos[] = {"SALDO", "TRANSACCION", "FECHA DE TRANSACCION"};
        DefaultTableModel ModeloT = new DefaultTableModel(null, Titulos);
        CLsCuentaUsuario clsCuentas = new CLsCuentaUsuario();
        ArrayList<Cuentausuario> Cuentas = clsCuentas.MostrarCuentasUsuario(IdUsuario);
        String filas[] = new String[4];
        SaldoTotal = 0;
        for (var IterarCuentas : Cuentas) {
            filas[0] = String.valueOf(IterarCuentas.getSaldo());
            String Transaccion = String.valueOf(IterarCuentas.getTransaccion());
            if (Transaccion.equals("1")) {
                filas[1] = "Abono";
                SaldoTotal = SaldoTotal + IterarCuentas.getSaldo();
            } else {
                filas[1] = "Cargo";
                SaldoTotal = SaldoTotal - IterarCuentas.getSaldo();
            }
            filas[2] = String.valueOf(IterarCuentas.getFecha());
            ModeloT.addRow(filas);
        }
        tbcuentas.setModel(ModeloT);
        lblSaldo.setText(String.valueOf(SaldoTotal));
    }
